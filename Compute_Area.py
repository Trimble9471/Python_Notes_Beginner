#Description: Computing the area of a Circle
'''
Algorithms can be described in natural languages or in pseudocode

Example:
Step 1: Get the circles radius (hard code later we will prompt the user)
Step 2: Compute the area applying the following formula:
        area = radius * radius * 3.14159
Step 3: Display the results
'''
#Create a variable
#Note: A variable is a name that references a value stored in memory
#Step 1 assign a value to a variable
radius = 20 #Radius is now 20

#Note: Python determines the data type by the value its assigned

#Step 2: Compute the area
area = radius * radius * 3.14159

#Step 3: Display the results
print("The area for the circle of radius", radius, "is", area)
print(f"The area for the circle of radius {radius} is {area}")

